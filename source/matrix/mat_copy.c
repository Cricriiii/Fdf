#include "fdf.h"

void	mat_copy(t_mat4x4 dest, t_mat4x4 src)
{
	int	i;
	int	j;
	
	if (!dest || !src)
		return ;
	i = -1;
	while (++i < 4)
	{
		j = -1;
		while (++j < 4)
		{
			dest->mat[i][j] = src->mat[i][j];
		}
	}	
}
