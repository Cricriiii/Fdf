#include "fdf.h"

t_mat4x4	identity(void)
{
    t_mat4x4 	id;
	
	id = ft_calloc(1, sizeof(struct s_mat4x4));
    if (id)
    {
        id->mat[0][0] = 1;
        id->mat[1][1] = 1;
        id->mat[2][2] = 1;
        id->mat[3][3] = 1;
    }
    return (id);
}
